user_names = ['John', 'Tere', 'Michael', 'Molly', 'Ramiro', 'Jackson']

print(user_names) #Imprime todos los elementos de la lista
print(user_names[0]) #Obtienes el primer elemento de la lista
print(user_names[3]) #Obtienes el cuarto elemento de la lista
print(user_names[-1]) #Obtendras el ultimo elemento de la lista
print(user_names[0:2]) #Obtendras los dos primeros elementos de la lista
print(user_names[0:4]) #Obtendras los cuatro primeros elementos de la lista
print(user_names[2:]) #Obtendras apartir del tercer elemento hasta el final de la lista
print(user_names[:3]) #Obtendras del inicio de la lista hasta el tercer elemento
print(user_names[:]) #Obtendras todos los elementos de la lista